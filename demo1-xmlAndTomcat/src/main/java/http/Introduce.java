public class Introduce {

    /*
    * 交互方式
    *   1、请求：永远都是客户端向服务端发送
    *   2、响应：永远都是服务端向哭护短返回
    * 数据的格式
    *   请求发送的数据 称之为请求报文
    *   响应时返回的数据 称之为响应报文
    *   报文有规定的格式
    *       报文首部
    *           行
    *           头
    *       报文主体
    *
    * 常见响应码：
    *   200：请求成功
    *   302：重定向
    *   304：使用了本地缓存
    *   404：请求的资源没有找到
    *   405：请求的方式不允许
    *   500：请求的资源找到了，但是服务器内部出现了问题
    * */

}
